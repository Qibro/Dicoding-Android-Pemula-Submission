package com.example.myfavoritegames;

public class Games {
    private String gameName;
    private String gameDetail;
    private String gameDev;
    private String gamePublisher;
    private String gameRelease;
    private String gamePlatform;
    private String gameGenre;
    private String gameModes;

    public String getGamePublisher() {
        return gamePublisher;
    }

    public void setGamePublisher(String gamePublisher) {
        this.gamePublisher = gamePublisher;
    }

    public String getGameRelease() {
        return gameRelease;
    }

    public void setGameRelease(String gameRelease) {
        this.gameRelease = gameRelease;
    }

    public String getGamePlatform() {
        return gamePlatform;
    }

    public void setGamePlatform(String gamePlatform) {
        this.gamePlatform = gamePlatform;
    }

    public String getGameGenre() {
        return gameGenre;
    }

    public void setGameGenre(String gameGenre) {
        this.gameGenre = gameGenre;
    }

    public String getGameModes() {
        return gameModes;
    }

    public void setGameModes(String gameModes) {
        this.gameModes = gameModes;
    }

    private int gamePhoto;

    public String getGameName() {
        return gameName;
    }

    public void setGameName(String gameName) {
        this.gameName = gameName;
    }

    public String getGameDetail() {
        return gameDetail;
    }

    public void setGameDetail(String gameDetail) {
        this.gameDetail = gameDetail;
    }

    public int getGamePhoto() {
        return gamePhoto;
    }

    public void setGamePhoto(int gamePhoto) {
        this.gamePhoto = gamePhoto;
    }


    public String getGameDev() {
        return gameDev;
    }

    public void setGameDev(String gameDev) {
        this.gameDev = gameDev;
    }


}
